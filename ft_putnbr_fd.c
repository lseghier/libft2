void	ft_putnbr_fd(int nb, int fd)
{
	char	*str;

	str = "0123456789";
	if (nb == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		write(fd, "-", 1);
		nb = -nb;
	}
	if (nb >= 10)
		ft_putnbr_fd(nb / 10, fd);
	write(fd, &str[nb % 10], 1);
}
